package com.gain.vulkan

import androidx.recyclerview.widget.RecyclerView
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.Button
import android.widget.TextView
import androidx.fragment.app.Fragment

import com.gain.vulkan.placeholder.PlaceholderContent.PlaceholderItem
import com.gain.vulkan.databinding.FragmentChooseBinding

/**
 * [RecyclerView.Adapter] that can display a [PlaceholderItem].
 * TODO: Replace the implementation with code for your data type.
 */
class MyItemRecyclerViewAdapter(
    private val values: List<PlaceholderItem>,
    private val onItemClick:(Fragment)->Unit
) : RecyclerView.Adapter<MyItemRecyclerViewAdapter.ViewHolder>() {

    override fun onCreateViewHolder(parent: ViewGroup, viewType: Int): ViewHolder {

        return ViewHolder(
            FragmentChooseBinding.inflate(
                LayoutInflater.from(parent.context),
                parent,
                false
            )
        )

    }

    override fun onBindViewHolder(holder: ViewHolder, position: Int) {
        val item = values[position]
        holder.contentView.text = item.content
        holder.contentView.setOnClickListener{
            onItemClick(item.fragment)
        }
    }

    override fun getItemCount(): Int = values.size

    inner class ViewHolder(binding: FragmentChooseBinding) : RecyclerView.ViewHolder(binding.root) {
        val contentView: Button = binding.mBtn

        override fun toString(): String {
            return super.toString() + " '" + contentView.text + "'"
        }
    }

}